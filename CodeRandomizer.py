import random

def randomize_tax_codes(codes):
    groups = {}
    
    for code in codes:
        numeric_part = ''.join(filter(str.isdigit, code))
        if numeric_part not in groups:
            groups[numeric_part] = []
        groups[numeric_part].append(code)
    
    all_numeric_parts = list(groups.keys())
    random.shuffle(all_numeric_parts)
    
    randomized_codes = []
    while all_numeric_parts:
        
        batch_size = min(12, len(all_numeric_parts))
        batch_parts = all_numeric_parts[:batch_size]
        all_numeric_parts = all_numeric_parts[batch_size:]
        
      
        batch_codes = []
        for part in batch_parts:
            batch_codes.extend(groups[part])
        
     
        random.shuffle(batch_codes)
        
   
        randomized_codes.extend(batch_codes)
    
    return randomized_codes

